    <h3>Why keyhole ? </h3>
</div>    
    <p>Keyhole /MICS procedures have many advantages to their credit 
</p>
    <ul class="bult_sec">
    <li>Small surgical wounds make one’s surgical experience great</li>
    <li>Significantly less pain experience compared to the conventional procedures</li>
    <li>Greatly reduced need for blood and blood product transfusion</li>
    <li>Abbreviated hospital stay and early resumption of the routine</li>
    <li>Excellent cosmetic outcome</li>
    <li>Very less wound related complications </li>
    
    </ul>
</div>   
  
    <div class="content_row">
<div class="home_comen_title">
    <h3> For which disease / diseases is / are these surgeries performed?  </h3>
</div>    
    <p>The keyhole/ MICS procedures are performed for a broad spectrum of diseases. 
</p>
<div class="sur_performedrow">
 <p><span>Firstly,</span> the commonest and the leading killer disease which affects either one , two or all
three of the blood vessels within the heart musculature, is the coronary artery disease. In
around 20-25% of such patients bypass procedure is the treatment of choice. The same
procedure which has been performed all along with the open technique , can be done with
the same precision and no less effect on its long term outcome, using the keyhole strategy
by experts in this field.</p>  
<div class="row">
<div class="col-lg-5 col-md-5">
          <div class="img_alb_box">
