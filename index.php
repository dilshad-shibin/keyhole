</div>
</div> 
<div class="sur_performedrow">
 <p><span>The next group</span> is the yet another spectrum of disease conditions that affect our heart
since birth. Commonly addressed as the ‘hole in the heart’, these are conditions allow
admixture of oxygenated and deoxygenated blood inside our heart. Some of these
conditions, like ASD (atrial septa defect) can also be repaired through the keyhole
approach.</p>  
