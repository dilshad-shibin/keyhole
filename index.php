</p>
<p>Patients are assessed for their surgical fitness with the following tests </p>
    <ul class="bult_sec">
    <li>Blood panel</li>
    <li>Urine screen</li>
    <li>Serum screen</li>
    <li>X ray of chest</li>
    <li>Imaging of abdominal viscera and chest</li>
    <li>Pulmonary function test</li>
    <li>Assessment of the carotid and peripheral arteries</li>
    </ul>
    <p>Once the team decides on the procedure, the patient undergoes stabilisation of their
existing medical conditions before their in house admission, the day prior of the surgery .
The immediate post operative period is spent in the ICU and the rest in the ward. The
average ICU stay on average is 1-2days and the total hospital stay is on an average 4-5days.
Our patients spring back to their routine within a fortnight and for become fit for even
international air travel within this time frame....</p>
