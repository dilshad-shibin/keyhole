              <h4>KEYHOLE BYPASS SURGICAL SCAR </h4>
              </div>
          </div>  
        </div>  
    
</div>
</div>   
<div class="sur_performedrow">
 <p><span>The second group</span> of conditions are those that affect the valves within our heart, which can
either shrink or leak, causing impediment to the natural path of blood flow. Such valves
were traditionally replaced after removing one’s own valve with a prosthesis. Such
diseased valves can be made as functional as their pre diseased status by repairing them
through small incisions. This alleviates the need for anticoagulant medications with narrow
safety margin. Interestingly, valves can be replaced as well through keyhole approach.</p>  
